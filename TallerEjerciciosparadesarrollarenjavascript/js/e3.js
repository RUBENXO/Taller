/*Se tiene un horno en casa con temperaturas en grados celsius
(centígrado), se requiere transformar la temperatura de 70°C a grados
Fahrenheit. Para ello tenga en cuenta la siguiente fórmula.
°F = (°C · 1,8) + 32*/
let gradosCelsius = 70
let gradosFahrenheit = (gradosCelsius * 1.8) + 32
console.log(`${gradosCelsius}°C es igual a ${gradosFahrenheit}°F`)
